    public void RegistrarProducto() {
        int item = 0;
        DefaultTableModel modelo = new DefaultTableModel();
        item = item + 1;
        modelo = (DefaultTableModel) DetalleProductos.getModel();

        ArrayList lista = new ArrayList();
        lista.add(item);
        lista.add(CajaCodigo.getText());
        lista.add(CajaNombre.getText());
        lista.add(CajaCantidad.getText());
        lista.add(CajaPrecio.getText());
        lista.add(CajaTotal.getText());

        Object[] O = new Object[7];
        O[0] = lista.get(1);
        O[1] = lista.get(2);
        O[2] = lista.get(3);
        O[3] = lista.get(4);
        O[4] = lista.get(5);
        O[5] = lista.get(6);

        modelo.addRow(O);
        DetalleProductos.setModel(modelo);
    }
    
    private void VaciarYllenarProveedor(){
        ProveedoresDao proveDao= new ProveedoresDao();
        jComboBox4.removeAllItems();
        
        List<Proveedor> lista = proveDao.ListarProveedor();
        for (int i = 0; i < lista.size(); i++) {
            int id = lista.get(i).getIdproveedores();
            String Nombre = lista.get(i).getProveedor();
            jComboBox4.addItem(new Combo(id, Nombre).toString());
        }
    }

