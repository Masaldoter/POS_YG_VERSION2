    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        Importar im= new Importar();  
        if(!"".equals(jTextField3.getText())){
            
            if(jComboBox1.getSelectedIndex() == 0){
               im.CargarExcel(jTextField3.getText());   
            }else{
                im.CargarExcelBodega(jTextField3.getText());
            }
         
        }else{
            DesktopNotify.setDefaultTheme(NotifyTheme.Dark);
            DesktopNotify.showDesktopMessage("¡ERROR!","¡AÚN NO HA SELECCIONADO NINGÚN ARCHIVO!", DesktopNotify.ERROR, 4000L);
        }
    }//GEN-LAST:event_jButton19ActionPerformed

    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Formato XLSX(*.XLSX)","XLSX");
        JFileChooser archivo= new JFileChooser();
        archivo.addChoosableFileFilter(filtro);
        archivo.setFileFilter(filtro);
        archivo.setDialogTitle("SELECCIONE UN ARCHIVO");
        int ventana = archivo.showOpenDialog(null);
        if(ventana == JFileChooser.APPROVE_OPTION){
            File file= archivo.getSelectedFile();
            jTextField3.setText(file.toString());
        }
    }//GEN-LAST:event_jButton18ActionPerformed

