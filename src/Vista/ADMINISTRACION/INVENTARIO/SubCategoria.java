    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int Fila1 = jTable1.getSelectedRow();
        jTextField1.setText(jTable1.getValueAt(Fila1, 1).toString());
        jLabel1.setText(jTable1.getValueAt(Fila1, 0).toString());
        jButton1.setText("EDITAR");
        
        int Columna = jTable1.getColumnModel().getColumnIndexAtX(evt.getX());
        int Fila = evt.getY()/jTable1.getRowHeight();
        if(Fila < jTable1.getRowCount() && Fila >= 0 && Columna < jTable1.getColumnCount() && Columna >= 0){
            Object value = jTable1.getValueAt(Fila, Columna);
            if(value instanceof JButton jButton){
                jButton.doClick();
                JButton boton = jButton;
                if (boton.getName().equals("eliminarsubcategoria")) {
                    SCDAO = new Categoria_SubCategorias_Ubicaciones_DAO();
                    Boolean Estado = SCDAO.EliminarSubCategoria(jTable1.getValueAt(Fila1, 0).toString());
                    if (Estado == true) {
                        LimpiarCajas();
                        ActualizarTablaSubCategorias();
                    }
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    public static void ActualizarTablaSubCategorias() {
        //jTable1.setShowHorizontalLines(true);
        jTable1.setDefaultRenderer(Object.class, new RenderTablas());
        AS = new ActualizarTablasCategorias_SubCategorias_Ubicaciones();
        LimpiarTablaVenta(jTable1);
        DefaultTableModel modelo = new DefaultTableModel();
        modelo = (DefaultTableModel) jTable1.getModel();
        List<Modelo.SubCategoria> ListarPr = AS.ActualizarSubCategorias(jLabel2.getText());
        Object[] ob = new Object[3];
        for (int i = 0; i < ListarPr.size(); i++) {
            ob[0] = ListarPr.get(i).getIdsubcategoria();
            ob[1] = ListarPr.get(i).getNombreSubcategoria();
            ob[2] = ListarPr.get(i).getEliminar();
            modelo.addRow(ob);
           //[255,230,205]
        }
        jTable1.setModel(modelo);
    }
    
