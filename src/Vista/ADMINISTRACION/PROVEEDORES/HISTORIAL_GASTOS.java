    public void CARGAR_HISTORIAL(){
        jPanel2.removeAll();
        CONSULTAS_CAJA C_C= new CONSULTAS_CAJA();
        HISTORIAL_GASTO_PANEL H_G_P;
        List<Compras> ListarPr = C_C.Total_ENTRADAS_SALIDAS_DETALLES(NUMERO_CAJA, jComboBox1.getSelectedItem().toString());
        Object[] ob = new Object[4];
        for (int i = 0; i < ListarPr.size(); i++) {
            H_G_P= new HISTORIAL_GASTO_PANEL(String.valueOf(ListarPr.get(i).getIdCAJA()), String.valueOf(ListarPr.get(i).getTOTAL_COMPRA()),
                    ListarPr.get(i).getDESCRIPCION_COMPRAS(), ListarPr.get(i).getFECHA_HORA_COMPRA()+" POR: "+ListarPr.get(i).getUSUARIO_REGISTRO_COMPRA());
            jPanel2.add(H_G_P);
        jPanel2.updateUI();
        }
    }
